package com.company;

public class Main {

        public static void main(String[] args) {

                int[] arr = { 1, 2, 3, 4, 5};

                // Basically sorting the array by descending index
                for (int i = arr.length-1; i >= (arr.length - arr.length); i--) {
                        System.out.println(arr[i]);
                }
        }
}